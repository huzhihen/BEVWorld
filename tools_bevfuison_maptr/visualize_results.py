"""
BEVFusionMapTR 结果可视化脚本。

使用说明：
1. 先运行测试脚本并保存 pkl 结果：

   python tools_bevfuison_maptr/maptr_test.py \\
     projects/configs/bevfusion_maptr/bevfusion_maptr_nusc_r50_24ep_w_centerline_fusion.py \\
     work_dirs/bevfusion_maptr_nusc/latest.pth \\
     --out work_dirs/bevfusion_maptr_nusc/results.pkl \\
     --eval bbox chamfer

2. 再将 pkl 结果渲染为 BEV 图片：

   python tools_bevfuison_maptr/visualize_results.py \\
     projects/configs/bevfusion_maptr/bevfusion_maptr_nusc_r50_24ep_w_centerline_fusion.py \\
     work_dirs/bevfusion_maptr_nusc/results.pkl \\
     --out-dir work_dirs/bevfusion_maptr_nusc/vis \\
     --bbox-score-thr 0.3 \\
     --map-score-thr 0.4 \\
     --max-samples 50

输出内容：
- 每个样本保存一张 png，文件名为 ``序号_token.png``。
- 图片中会叠加 3D 检测预测框、3D GT 框、检测类别文字、MapTR 矢量地图预测线。
- 坐标系为 LiDAR BEV 俯视图，x 向前，y 向左，显示范围来自配置中的
  ``object_point_cloud_range``；地图矢量通常集中在 ``point_cloud_range`` 内。
"""

import argparse
import importlib
import os
from typing import Any

import matplotlib.pyplot as plt
import mmcv
import numpy as np
import torch
from mmcv import Config


DET_COLORS = [
    "#1f77b4", "#ff7f0e", "#2ca02c", "#d62728", "#9467bd",
    "#8c564b", "#e377c2", "#7f7f7f", "#bcbd22", "#17becf",
]
MAP_COLORS = ["orange", "blue", "red", "green", "purple", "cyan"]


def parse_args():
    parser = argparse.ArgumentParser(description="Visualize BEVFusionMapTR pkl results")
    parser.add_argument("config", help="config file path")
    parser.add_argument("result", help="result pkl generated by maptr_test.py --out")
    parser.add_argument("--out-dir", default="work_dirs/bevfusion_maptr_vis")
    parser.add_argument("--bbox-score-thr", type=float, default=0.3)
    parser.add_argument("--map-score-thr", type=float, default=0.4)
    parser.add_argument("--max-samples", type=int, default=-1)
    parser.add_argument("--no-gt", action="store_true", help="do not draw 3D GT boxes")
    parser.add_argument("--dpi", type=int, default=180)
    return parser.parse_args()


def import_plugin(cfg, config_path):
    if not getattr(cfg, "plugin", False):
        return
    if hasattr(cfg, "plugin_dir"):
        module_path = ".".join(cfg.plugin_dir.strip("/").split("/"))
    else:
        module_path = os.path.dirname(config_path).replace("/", ".")
    importlib.import_module(module_path)


def to_numpy(value: Any):
    if value is None:
        return None
    if hasattr(value, "tensor"):
        value = value.tensor
    if torch.is_tensor(value):
        return value.detach().cpu().numpy()
    if isinstance(value, np.ndarray):
        return value
    return np.asarray(value)


def get_box_corners_bev(boxes):
    """Convert LiDAR boxes [x, y, z, dx, dy, dz, yaw] to BEV 4-corners."""
    boxes = to_numpy(boxes)
    if boxes is None or boxes.size == 0:
        return np.zeros((0, 4, 2), dtype=np.float32)

    centers = boxes[:, :2]
    dims = boxes[:, 3:5]
    yaws = boxes[:, 6]
    template = np.array(
        [[0.5, 0.5], [0.5, -0.5], [-0.5, -0.5], [-0.5, 0.5]],
        dtype=np.float32,
    )
    corners = []
    for center, dim, yaw in zip(centers, dims, yaws):
        local = template * dim.reshape(1, 2)
        rot = np.array(
            [[np.cos(yaw), -np.sin(yaw)], [np.sin(yaw), np.cos(yaw)]],
            dtype=np.float32,
        )
        corners.append(local @ rot.T + center.reshape(1, 2))
    return np.asarray(corners)


def draw_boxes(ax, result, class_names, score_thr):
    boxes = result.get("boxes_3d")
    scores = to_numpy(result.get("scores_3d"))
    labels = to_numpy(result.get("labels_3d"))
    corners = get_box_corners_bev(boxes)
    if corners.size == 0:
        return

    if scores is None:
        scores = np.ones((corners.shape[0],), dtype=np.float32)
    if labels is None:
        labels = np.zeros((corners.shape[0],), dtype=np.int64)

    keep = scores >= score_thr
    for box_corners, score, label in zip(corners[keep], scores[keep], labels[keep]):
        label = int(label)
        color = DET_COLORS[label % len(DET_COLORS)]
        closed = np.concatenate([box_corners, box_corners[:1]], axis=0)
        ax.plot(closed[:, 0], closed[:, 1], color=color, linewidth=1.0)
        class_name = class_names[label] if label < len(class_names) else str(label)
        ax.text(
            box_corners[0, 0],
            box_corners[0, 1],
            f"{class_name}:{score:.2f}",
            color=color,
            fontsize=4,
        )


def draw_gt_boxes(ax, ann_info, class_names):
    boxes = ann_info.get("gt_bboxes_3d")
    labels = to_numpy(ann_info.get("gt_labels_3d"))
    corners = get_box_corners_bev(boxes)
    if corners.size == 0:
        return

    for box_corners, label in zip(corners, labels):
        label = int(label)
        if label < 0:
            continue
        closed = np.concatenate([box_corners, box_corners[:1]], axis=0)
        ax.plot(closed[:, 0], closed[:, 1], color="black", linewidth=0.8, linestyle="--")
        class_name = class_names[label] if label < len(class_names) else str(label)
        ax.text(
            box_corners[2, 0],
            box_corners[2, 1],
            f"GT:{class_name}",
            color="black",
            fontsize=4,
        )


def draw_map_vectors(ax, result, map_classes, score_thr):
    pts = to_numpy(result.get("map_pts_3d", result.get("pts_3d")))
    scores = to_numpy(result.get("map_scores_3d", result.get("scores_3d")))
    labels = to_numpy(result.get("map_labels_3d", result.get("labels_3d")))
    if pts is None or pts.size == 0:
        return
    if scores is None:
        scores = np.ones((pts.shape[0],), dtype=np.float32)
    if labels is None:
        labels = np.zeros((pts.shape[0],), dtype=np.int64)

    for line, score, label in zip(pts, scores, labels):
        if score < score_thr:
            continue
        line = np.asarray(line)
        if line.ndim != 2 or line.shape[0] < 2:
            continue
        label = int(label)
        color = MAP_COLORS[label % len(MAP_COLORS)]
        ax.plot(line[:, 0], line[:, 1], color=color, linewidth=1.6, alpha=0.9)
        class_name = map_classes[label] if label < len(map_classes) else str(label)
        ax.text(line[0, 0], line[0, 1], f"{class_name}:{score:.2f}", color=color, fontsize=4)


def main():
    args = parse_args()
    cfg = Config.fromfile(args.config)
    import_plugin(cfg, args.config)

    from mmdet3d.datasets import build_dataset

    cfg.data.test.test_mode = True
    dataset = build_dataset(cfg.data.test)
    results = mmcv.load(args.result)
    mmcv.mkdir_or_exist(args.out_dir)

    class_names = list(getattr(dataset, "CLASSES", cfg.get("object_classes", [])))
    map_classes = list(getattr(dataset, "MAPCLASSES", cfg.get("map_classes", [])))
    pc_range = cfg.get("object_point_cloud_range", cfg.get("point_cloud_range"))
    xlim = [pc_range[0], pc_range[3]]
    ylim = [pc_range[1], pc_range[4]]

    max_samples = len(results) if args.max_samples < 0 else min(args.max_samples, len(results))
    for idx in mmcv.track_iter_progress(range(max_samples)):
        result = results[idx]
        token = dataset.data_infos[idx].get("token", f"{idx:06d}")

        fig, ax = plt.subplots(figsize=(8, 8))
        ax.set_title(f"{idx:06d}  {token}")
        ax.set_aspect("equal", adjustable="box")
        ax.set_xlim(xlim)
        ax.set_ylim(ylim)
        ax.set_xlabel("x / forward (m)")
        ax.set_ylabel("y / left (m)")
        ax.grid(True, linestyle="--", linewidth=0.3, alpha=0.5)

        draw_map_vectors(ax, result, map_classes, args.map_score_thr)
        draw_boxes(ax, result, class_names, args.bbox_score_thr)
        if not args.no_gt and hasattr(dataset, "get_ann_info"):
            draw_gt_boxes(ax, dataset.get_ann_info(idx), class_names)

        out_path = os.path.join(args.out_dir, f"{idx:06d}_{token}.png")
        fig.tight_layout()
        fig.savefig(out_path, dpi=args.dpi)
        plt.close(fig)

    print(f"Visualization saved to: {args.out_dir}")


if __name__ == "__main__":
    main()
